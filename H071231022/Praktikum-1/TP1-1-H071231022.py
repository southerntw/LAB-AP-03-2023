z = (x**2+y**2)**0.5 #rumus phytagoras untuk cari miring
keliling = x+y+z #mencari keliling di jumlahkan semua variabel

print(f'luas permukaan = {luas:.2f}') 
print(f'keliling ={keliling: .2f}') #spy cmn 2 angka diblkang koma



